public class Calculator {
	public int add(int a, int b){
		int result = a + b;
		return result;
	}
	
	public int sub(int a, int b){
		int result = a - b;
		return result;
	}
	
	public int mul(int a, int b){
		int result = a * b;
		return result;
	}
	
	public double div(int a, int b){
		double result = (double) a / ( (double) b);
		return result;
	}
	
}